package Collage_Class.PseudoCode.Function;
public class ProcedureAdd {
    public static void add(int a, int b) {
        System.out.println(a + b);
    }

    public static void main(String[] args) {
        add(2, 3);
    }
}

/*
PSEUDOCODE

PROCEDURE add(a: INTEGER, b: INTEGER)
    PRINT a + b
END PROCEDURE

CALL add(2, 3)
*/