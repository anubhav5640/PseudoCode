package Collage_Class.PseudoCode.IF_ELSE;

public class Even_Odd {
    public static void main(String[] args) {
        int num = 20;
        if(num % 2 == 0){
            System.out.println("EVEN");
        }
        else{
            System.out.println("ODD");
        }
    }
}
